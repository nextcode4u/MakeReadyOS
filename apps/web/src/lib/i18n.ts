export type TranslationKey = keyof typeof translations.en;

export function normalizeLanguage(language: UserLanguage | string | undefined): UserLanguage {
  return language === "es" ? "es" : "en";
}

export function localeForLanguage(language: UserLanguage | string | undefined) {
  return normalizeLanguage(language) === "es" ? "es-US" : "en-US";
}

export function t(language: UserLanguage | string | undefined, key: TranslationKey) {
  const normalized = normalizeLanguage(language);
  return translations[normalized][key] ?? translations.en[key] ?? key;
}

export function tWithVars(language: UserLanguage | string | undefined, key: TranslationKey, vars: Record<string, string | number>) {
  return Object.entries(vars).reduce((message, [name, value]) => message.split(`{${name}}`).join(String(value)), t(language, key));
}

export function translateUserRole(language: UserLanguage | string | undefined, role: UserRole) {
  return t(language, `role.${role}` as TranslationKey);
}
